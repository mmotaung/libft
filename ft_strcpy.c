#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	char	*p;

	p = dst;
	while (*src != '\0')
	{
		*dst = *src;
		dst++;
		src++;
	}
	*dst = '\0';
	return (p);
}
