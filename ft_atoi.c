#include "libft.h"

int		ft_atoi(const char *str)
{
	long	i;
	int		j;

	i = 0;
	while ((*str >= '\t' && *str <= '\r') || (*str == ' '))
		str++;
	if (*str == '-')
		j = -1;
	else
		j = 1;
	if (*str == '-' || *str == '+')
		str++;
	while (*str >= '0' && *str <= '9')
		i = (i * 10) + (*str++ - '0');
	if (i < 0 && j == 1)
		return (-1);
	if (i < 0)
		return (0);
	return (i * j);
}
