#include "libft.h"

int		ft_atoi(const char *s)
{
	unsigned int		i;
	unsigned int		j;
	unsigned int		n;

	i = 0;
	j = 0;
	n = 0;
	while (s[i] == '\t' || s[i] == '\v' ||
			s[i] == '\r' || s[i] == '\f' ||
			s[i] == '\n' || s[i] == ' ')
		i++;
	if (s[i] == '-')
		j = 1;
	if (s[i] == '-' || s[i] == '+')
		i++;
	while (s[i] >= '0' && s[i] <= '9')
	{
		n = n * 10 + (s[i] - '0');
		i++;
	}
	if (j == 1)
		return (-n);
	return (n);
}
